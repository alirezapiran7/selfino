package ir.selfino.android.Function.Models;

public class PackageOption
{
    private int silver_status;
    private int free_status;
    private int gold_status;
    private String option_name;

    public int getSilver_status()
    {
        return silver_status;
    }

    public void setSilver_status(int silver_status)
    {
        this.silver_status = silver_status;
    }

    public int getFree_status()
    {
        return free_status;
    }

    public void setFree_status(int free_status)
    {
        this.free_status = free_status;
    }

    public int getGold_status()
    {
        return gold_status;
    }

    public void setGold_status(int gold_status)
    {
        this.gold_status = gold_status;
    }

    public String getOption_name()
    {
        return option_name;
    }

    public void setOption_name(String option_name)
    {
        this.option_name = option_name;
    }
}
